package ProjetoWebMotors;

public class Pessoa {
    protected String nome;
    protected String cpf;
    protected String cnpj;
    protected String email;
    protected String telefone;
    protected String regiao;

    public Pessoa ( String nome,String cpf,String cnpj,String email,String telefone,String regiao){
        this.nome = nome;
        this.cpf = cpf;
        this.cnpj = cnpj;
        this.email = email;
        this.telefone = telefone;
        this.regiao = regiao;}
}
